async function setContract(address){
  return await new window.web3.eth.Contract(
    contractABI, rinkebyAddress
  );
}

async function getCurrentAccount() {
  const accounts = await window.web3.eth.getAccounts();
  return accounts[0];
}

async function mint(){
  window.contract = await setContract(rinkebyAddress);
  const account = await getCurrentAccount();
  await window.contract.methods.mint().send({from:account, value:"100000000000000"});
}

async function loadWeb3() {
    if (window.ethereum) {
        window.web3 = new Web3(window.ethereum);
        window.ethereum.enable();
    }
}

async function load() {
    await loadWeb3();
}

